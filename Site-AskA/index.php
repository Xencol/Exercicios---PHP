<h1>Acessibilidade Auditiva em sites</h1>

<h2 class="mainsub">Qual a sua importância?</h2>

<div class="flex1">


<div>
<p>A acessibilidade auditiva em sites é fundamental para a inclusão de pessoas com deficiência auditiva, promovendo a igualdade de acesso à informação e à interação online. Ao garantir que os conteúdos estejam acessíveis, os sites se tornam mais inclusivos e usáveis para todos.
Quando ambientes e conteúdo não são acessíveis, pessoas com deficiência auditiva enfrentam barreiras que limitam sua capacidade de se comunicar, aprender e interagir. Isso pode resultar em exclusão social e profissional, além de impactar negativamente a autoestima e a qualidade de vida.</p>
</div>

<div id="divdireita">
<p>No Brasil, a Lei Brasileira de Inclusão (Lei nº 13.146/2015) estabelece diretrizes para a promoção da acessibilidade em diversos aspectos da vida pública e privada. Essa legislação inclui a acessibilidade auditiva, exigindo que instituições públicas e privadas adotem medidas que garantam a comunicação efetiva com pessoas surdas ou com deficiência auditiva. Além disso, normas técnicas, como a ABNT NBR 9050, orientam sobre a implementação de recursos acessíveis em edificações e espaços públicos.</p>
</div>
 
</div>
 
<h2>Recursos de Acessibilidade Auditiva</h2>
 
<div class="recurso">
 
<h3>Legenda em vídeos:</h3>
 
<p>Permitem que pessoas com perda auditiva acompanhem o conteúdo visual, especialmente em vídeos informativos ou educativos.</p>
 
<h3>Transcrições de Áudio:</h3>
 
<p>Oferecem um registro textual do conteúdo de áudio, facilitando a compreensão para pessoas com deficiência auditiva. </p>
 
<h3>Tradução em Libras:</h3>
 
<p>A tradução em Libras garante que pessoas surdas ou com deficiência auditiva possam compreender o conteúdo de um site que usa essa língua como forma de comunicação. </p>
 
<h3>Intérpretes de Libras:</h3>
 
<p>A presença de intérpretes de Libras em eventos virtuais, apresentações ou reuniões on-line garante que pessoas surdas ou com deficiência auditiva possam interagir e acompanhar o conteúdo em tempo real.</p>
 
<h3>Sinalização Visual:</h3>
 
<p>A utilização de sinais visuais e indicadores em vez de sons é importante para informar sobre alarmes, chamadas ou anúncios sonoros.</p>
 
<h3>Comunicação Escrita:</h3>
 
<p>Sempre que possível, fornecer informações importantes por escrito, além de sons ou áudios, facilita a compreensão. </p>
 
<h3>Tecnologia Assistiva:</h3>
 
<p>Incentivar o uso de tecnologias assistivas, como aparelhos auditivos, implantes cocleares e outros dispositivos, garante que pessoas com deficiência auditiva possam acessar informações com maior facilidade.</p>
 
<h3>Plugins de Acessibilidade:</h3>
 
<p>Existem plugins, como o do Hand Talk, que traduzem o conteúdo do site para Libras, tornando-o acessível para pessoas surdas.</p>
</div>


