<h1>VERCEL</h1>

<p><q>O Vercel disponibiliza ferramentas e estrutura em nuvem para construir, escalar, e garantir uma página web mais segura e rápida.</q></p>

<h2>O que é o Vercel?</h2>
 <p>O Vercel é uma plataforma para hospedagem de sites e aplicativos front-end com integração de frameworks como <a href="http://nextjs.org/" target="_blank">Next.js</a> e <a href="https://nuxt.com/" target="_blank">Nuxt</a>. Graças a essa interação com os frameworks, ele é ideal para desenvolvedores front-end que precisam de velocidade em seus deploys (ação de publicar o site na internet), buscam escalabilidade e uma interface intuitiva para realizar suas hospedagens.</p>
  
<h2>Recursos interessantes</h2>

<h3>Facilidade no deploy:</h3>
 <p>O Vercel é uma das plataformas mais simples para efetuar o deploy, podemos fazer isso apenas adicionarmos o main branch no domínio da plataforma.</p>

<h3>Criar um domínio customizável:</h3>
<p>A plataforma habilita os desenvolvedores a criar seus próprios domínios (ou uma URL gratuita) e uma SSL gratuita, protegendo informações compartilhadas entre o servidor e o browser de forma encriptada.</p>

<h3>Deploy de sites e aplicações em cada push do Github:</h3>
<p>Graças a integração direta com o Github e o GitLab, os desenvolvedores podem hospedar um site de modelo static e aplicações web que realizam deploy através de quaisquer pushs nas branchs.</p>

<h2>Benefícios</h2>
 <p>Fazer um deploy no Vercel é muitos simples. Não há necessidade de lidas com problemas como indisponibilidade de servidores, já que hospedar o seu site na plataforma significa que os deploys são instantâneos. A escalabilidade já é também configurada por padrão, assim evitando problemas quando seu site ficar famoso.</p>

 <p>A plataforma também cuida de todo trabalho administrativo para garantir que o seu site esteja rodando de forma suave. Também disponibiliza uma abordagem personalizável em relação à localização dos usuários do site, coletando informações de como os visitantes de diversas regiões se comportam no site.</p>

<h2>Como usar?</h2>
<p>Para realizar um deploy na plataforma, o desenvolvedor deve criar um projeto que agrupa os deployments e domínios customizáveis juntos. Cada deplayment vai receber requisições do Github, GitLab, Bitbucket, ou Vercel CLI.</p>
<p>Podemos criar um projeto diretamente do dashborad do Vercel ou importar de um repositório Git já existente. Você pode editar o nome do projeto, desenvolver configurações, modificar o diretório root e configurar outras variáveis.</p>

<h2>Preço?</h2>
<p>Todos os benefícios mostrados aqui podem ser utilizados de forma gratuita para sites não comerciais, logo a plataforma não cobra nada para usuários que estejam hospedando seus sites ou aplicações em nuvem.</p>

<h1>Glitch</h1>

<p>O Glich.com é uma plataforma desenvolvida pelo mesmo time que fundou o Trello e o Stack Overflow.
A plataforma Glitch é um lugar onde os desenvolvedores podem criar um projeto completo: de uma página web static com apenas HTML/CSS, ou um projeto um projeto front-end com Angular/React  ou uma página web full-stack usando NodeJS.
 O objetivo da Glitch é criar uma comunidade onde desenvolvedores e designers podem se unir para desenvolver em conjunto e aprender com o código de cada um.</p>

<h2>Recursos do Glitch</h2>

<h3>Suporte para codificação em equipes:</h3>
<p>Você pode convidar amigos para participar da codificação sem que eles precisem instalar uma IDE.</p>

<h3>Controle de versões:</h3>
<p>O Glitch tem de forma embutida um controle de versionamento, assim não é necessário a utilização de Git para atualizar o projeto.</p>

<h3>Disponibilidade web enquanto desenvolve:</h3>
<p>uando o desenvolvedor está criando um projeto no Glitch, ele já tem um subdomínio.</p>

<h3>Frameworks:</h3>
<p>A plataforma tem diversos frameworks disponíveis para facilitar a vida do desenvolvedor, desde React/Angular até Slack App.</p>

<h3>Preço?</h3>
<p>O plano inicial da plataforma é gratuito, embora seja limitado em comparação ao plano Pro, ele oferece diversas formas de auxiliar o dev, como:
<ul class="list">
<li>Os código são open-source por padrão;</li>
<li>Sites static ilimitados que estão sempre em funcionamento;</li>
<li>Suporte a import/export do GitHub e Prettier.</li>
</ul>
</p>


